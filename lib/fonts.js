const allFonts = {
  fancy1,
  strikeThrough,
  typewriter,
  fancy30,
  fancy31,
 fancy32,
  fancy33
};

// Convert plain text using selected font
function stylize(text, fontName) {
  const font = allFonts[fontName];
  if (!font) return text;

  return text.split('').map(char => {
    const lower = char.toLowerCase();
    return font[lower] || char;
  }).join('');
}

export { allFonts, stylize };
